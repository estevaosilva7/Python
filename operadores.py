a = 10
b = 2


# Aritméticos

print(a + b) # soma
print(a - b) # subtração
print(a * b) # multiplicação
print(a / b) # divisão (resultado com float)
print(a // b) # divisão inteira
print(a % b) # resto da divisão 
print(a ** b) # potência
print("==================")


# Relacionais

print(a > b) # maior que 
print(a < b) # menor que
print(a == b) # igual
print(a != b) # diferente
print(a >= b) # maior ou igual
print(a <= b) # menor ou igual
print("==================")


# Lógicos

x = True
y = False


print(x and y) # E (True se ambos forem True)
print(x or y) # OU (True se pelo menos um for True)
print(not x) # NÃO (Inverte o valor)